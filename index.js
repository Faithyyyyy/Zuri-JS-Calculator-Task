
let number1 = parseInt (prompt('Input first Number'));
let operator = prompt('Enter operator (+, -, *, /)');
let number2 = parseInt (prompt('Input Second Number'));


if(operator ==="+"){
    Alert(number1 + number2)
}else if (operator === "-"){
    Alert(number1 - number2)
}else if (operator === "*"){
    Alert (number1 * number2)
}else if (operator === "/"){
    Alert(number1 / number2)
}
